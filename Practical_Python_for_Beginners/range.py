# range() is a built-in Python function.
# It returns a sequence of numbers.
# Syntax: range(start, stop, step)

for i in range(7):  # by default, range starts at 0 and stops before the number defined in parenthesis.
    print(i)

for number in range(2, 15, 2):  # the sequence starts at 2, and stops at 14, skipping 2 
    print(number)


