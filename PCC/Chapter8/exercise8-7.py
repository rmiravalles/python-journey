# Creating a dictionary that stores album information using a function.

def make_album(title, artist):
    album = {'title': title, 'artist': artist}
    return album


print(make_album('Nevermind', 'Nirvana'))

print(make_album('Piece of Cake', 'Mudhoney'))

print(make_album('Loveless', 'My Bloody Valentine'))

