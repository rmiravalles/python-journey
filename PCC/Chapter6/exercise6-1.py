# A dict to store a friend's personal information.

friend = {
    'first_name': 'Bob',
    'last_name': 'Dylan',
    'age': 79,
    'city': 'Malibu',
    }

print("My friend's name is", friend['first_name'], friend['last_name'])
print("He's", friend['age'], "and lives in", friend['city'])

print(f"My friend's name is {friend['first_name']} {friend['last_name']}.")
