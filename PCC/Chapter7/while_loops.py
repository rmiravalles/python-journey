# While loops will run as long as a certain condition is true.

# Counting up through a series of numbers.

current_number = 1  # we start the count with number 1

while current_number <= 5:  # the while loop will keep running as long as the value of current_number is less than or equal to 5
    print(current_number)  # since 1 meets this criteria, we print the number 1
    current_number += 1  # we then add 1 to the current number, and if the condition is still true, the program keeps looping
